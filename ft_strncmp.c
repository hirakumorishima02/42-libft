#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && i < n)
	{
		if (s1[i] != '\0')
			i++;
		else
			return (0);
	}
	if (s1[i] > s2[i])
		return (1);
	return (-1);
}
